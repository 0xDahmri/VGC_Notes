window.calcSpeed = function (base) {
  const s   = Math.floor((2 * base + 31) * 0.5 + 5);
  const n32 = s + 32;
  const max = Math.floor(n32 * 1.1);
  return {
    neutral0:       s,
    neutral32:      n32,
    minus0:         Math.floor(s * 0.9),
    maxSpeed:       max,
    scarfMax:       Math.floor(max * 1.5),
    scarfNeutral32: Math.floor(n32 * 1.5),
  };
};
